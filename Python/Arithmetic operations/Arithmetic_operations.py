# Addition
ans = 10 + 9 
print(ans) # 19 

# Subtraction
ans = 9 - 10
print(ans) # -1 

# Multiplication
ans = 9 * 10
print(ans) # 90

# Division
ans = 8/9
print(ans) # 0.8888888888888888

# Floor division
ans = 30 // 9 
print(ans) # 3

# Modulo 
ans = 10 % 5
print(ans) # 0 
ans = 10 % 6
print(ans) # 4

# Exponentiation
ans = 5 ** 5
print(ans) # 3125
ans = 81 ** 0.5
print(ans) # 9.0

# Augmented addition operator
ans = 5; 
ans += 14
print(ans)  # 19

# Augmented subtraction operator
ans = 5; 
ans -= 14
print(ans)  # -9 

# Augmented multiplication operator
ans = 5
ans *= 14
print(ans) # 70

# Augmented division operator
ans = 5
ans /= 14
print(ans) # 0.35714285714285715

# Augmented floor division operator
ans = 5
ans //= 14
print(ans) # 0

# Augmented modulo operator
ans = 5
ans %= 14
print(ans) # 5

# Augmented exponentiation operator
ans = 5
ans **= 14
print(ans) # 6103515625

# Operator precedence
expr = (5 - 3) * 234 ** 2 + 4 
# 2 * 234 ** 2 + 4
# 2 * 54756 + 4
# 109512 + 4
# 109516
print(expr) # 109516
